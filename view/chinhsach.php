    -Không đủ số lượng, không đủ bộ như trong đơn hàng. <br> <br>
    -Sản phẩm phải còn nguyễn nhãn mác, trong tình trạng còn mới / chưa qua sử dụng. <br> <br>
    Khách hàng có trách nhiệm trình giấy tờ liên quan chứng minh sự thiếu sót trên để hoàn thành việc hoàn
    trả/đổi trả hàng hóa. <br> <br>
    <b>2. Quy định về thời gian thông báo và gửi sản phẩm đổi trả <br></b> <br>
    <b> -Thời gian thông báo đổi trả</b>: trong vòng 3 ngày kể từ khi nhận sản phẩm đối với trường hợp sản phẩm
    thiếu phụ kiện, quà tặng hoặc bể vỡ. <br> <br>
    <b>-Thời gian gửi chuyển trả sản phẩm</b>: trong vòng 7 ngày kể từ khi nhận sản phẩm. <br> <br>
    <b>-Địa điểm đổi trả sản phẩm</b>: Khách hàng có thể mang hàng trực tiếp đến văn phòng/ cửa hàng của chúng
    tôi hoặc
    chuyển qua đường bưu điện. <br> <br>
    Trong trường hợp Quý Khách hàng có ý kiến đóng góp/khiếu nại liên quan đến chất lượng sản phẩm, Quý Khách
    hàng vui lòng liên hệ fanpage của TXT. Xin cảm ơn quý khách.
